public class BTNode {
    private int data;
    private BTNode left, right;

    public BTNode(int data) {
        this.data = data;
    }

    public int getData() {
        return data;
    }

    public BTNode getLeft() {
        return left;
    }

    public void setLeft(BTNode left) {
        this.left = left;
    }

    public BTNode getRight() {
        return right;
    }

    public void setRight(BTNode right) {
        this.right = right;
    }
}